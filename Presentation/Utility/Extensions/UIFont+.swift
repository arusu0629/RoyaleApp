import Foundation
import UIKit

extension UIFont {

    static func HiraginoSansW6(size: CGFloat) -> UIFont {
        return UIFont(name: "HiraginoSans-W6", size: size)!
    }

    static func HiraginoSansW7(size: CGFloat) -> UIFont {
        return UIFont(name: "HiraginoSans-W7", size: size)!
    }

}
